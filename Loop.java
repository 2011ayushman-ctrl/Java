public class Loop {
    public static void main(String[] args) {
        //using for loop
        for (int i=7;i>2;i--){
            System.out.println(i);
        }
        
        //using while loop
        int j = 8;
        while (j<10){
            System.out.println(j);
            j++;
        }
        }
}
    

